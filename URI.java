
public class URI {
	private String nome, posicao, pais, universidade, desde, pontos, resolvido, tentado, submissoes;
	protected URLLoad2 url;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		
		this.nome = nome;
	}
	public String getPosicao() {
		return posicao;
	}
	public void setPosicao(String posicao) {
		
		this.posicao = posicao;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		
		this.pais = pais;
	}
	public String getUniversidade() {
		return universidade;
	}
	public void setUniversidade(String universidade) {
		
		this.universidade = universidade;
	}
	public String getDesde() {
		return desde;
	}
	public void setDesde(String desde) {
		
		this.desde = desde;
	}
	public String getPontos() {
		return pontos;
	}
	public void setPontos(String pontos) {
		
		this.pontos = pontos;
	}
	public String getResolvido() {
		return resolvido;
	}
	public void setResolvido(String resolvido) {
		
		this.resolvido = resolvido;
	}
	public String getTentado() {
		return tentado;
	}
	public void setTentado(String tentado) {
		
		this.tentado = tentado;
	}
	public String getSubmissoes() {
		return submissoes;
	}
	public void setSubmissoes(String submissoes) {
		
		this.submissoes = submissoes;
	}
	public URLLoad2 getUrl() {
		return url;
	}
	public void setUrl(URLLoad2 url) {
		
		this.url = url;
	}


	
			
}
